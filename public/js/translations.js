window.getTranslation = function(key) {
    const lang = window.currentIdioma || localStorage.getItem('mp_lang') || 'es';
    return (window.TRANSLATIONS[lang] && window.TRANSLATIONS[lang][key])
        || (window.TRANSLATIONS['es'] && window.TRANSLATIONS['es'][key])
        || key;
};

